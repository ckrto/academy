package ex02;

public class Char {
	public static void charExample() {
		char ban = 'A';
		String name = "홍길동";
		System.out.println("반 : " + ban);
		System.out.println("이름 : " + name);
		
		ban = 'B';
		name = "심청이";
		System.out.println("반 : " + ban);
		System.out.println("이름 : " + name);
		
		ban = 'C';
		name = "강감찬";
		System.out.println("반 : " + ban);
		System.out.println("이름 : " + name);
		
	}
}
